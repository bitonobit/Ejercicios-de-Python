def saluda(name):
    """
    Esta función saluda a la persona con el nombre proporcionado.

    Args:
        name (str): El nombre de la persona a saludar.

    Returns:
        str: Un saludo personalizado con el nombre de la persona.
    """
    return f"Hola, {name}! ¿Cómo estás?"

# Llamada a la función
saluda("Candy")